# Set Datatype :- Set is the collection of the unordered items.
# --> Each element in the set must be unique & immutable.
# --> Notice :- Set Is Mutable But Set's Element Are Immutable

collection = {1, 2, 3, 4, "Hello", "World"}

print(collection)
print(type(collection))
print(len(collection))  # total number of items

# empty set syntax
null_set = set()
print(type(null_set))
